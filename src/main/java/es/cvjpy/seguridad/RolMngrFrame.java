    private final ModeloGuiPro auxiliar = new ModeloGuiPro(this);
    private String antid;

    public void refresh() {
        firePropertyChange(PROP_DATOS, null, datos);
    }

    private void seleccionado() {

        this.bqda.setVisible(false);
        this.setVisible(true);

    }
    private RolMngrCli datos;
    public static final String PROP_DATOS = "datos";

    public RolMngrCli getDatos() {
        return datos;
    }

    public void setDatos(RolMngrCli datos) {
        RolMngrCli oldDatos = this.datos;
        this.datos = datos;
        firePropertyChange(PROP_DATOS, oldDatos, datos);
    }

    @Override
    public void inicio(PrincipalUIPro padre) {
        auxiliar.inicio(padre);
        RolMngrCli prog = new RolMngrCli();
        prog.setPrincipalPro(padre.getServidor());
        prog.ultimo();
        setDatos(prog);
        mngrBarra1.setDatos(prog);
        bqdaBarra1.setDatos(prog);
        setVisible(true);
        refresh();
    }

    @Override
    public boolean puedosalir() {
        return auxiliar.puedosalir();
    }

    @Override
    public void fin() {
        if (puedosalir()) {
            auxiliar.fin();
        }
    }

    @Override
    public boolean isIniciado() {
        return auxiliar.isIniciado();
    }

    @Override
    public void rellama() {
        setState(0);
        setVisible(true);
    }
