    private void campoidFocusGained(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_campoidFocusGained

        this.antid = this.campoid.getText();
        this.campoid.setSelectionStart(0);
        this.campoid.setSelectionEnd(999);

    }//GEN-LAST:event_campoidFocusGained

    private void campoidFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_campoidFocusLost

        this.campoid.setSelectionEnd(0);
        String mitxt = this.campoid.getText();
        if (!mitxt.trim().equals(this.antid.trim())) {
            getDatos().keyCambiada();
            refresh();
        }

    }//GEN-LAST:event_campoidFocusLost

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked

        if (evt.getClickCount() > 1) {
            bqdaBarra1.botonseleccionar();
            refresh();
        }

    }//GEN-LAST:event_jTable1MouseClicked

    private void bqdaBarra1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bqdaBarra1ActionPerformed

        String cual = evt.getActionCommand().trim();

        if (cual.equals("ESC")) {
            bqda.setVisible(false);
        }
        if (cual.equals("F3")) {
            seleccionado();
        }
        refresh();

    }//GEN-LAST:event_bqdaBarra1ActionPerformed
